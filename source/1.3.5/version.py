    @classmethod
    def get_info(cls):
        return {
            "version": cls.CURRENT,
            "date": cls.RELEASE_DATE,
            "history": cls.HISTORY
        }

    @classmethod
    def print_version(cls):
        """Выводит информацию о версии в консоль"""
        info = cls.get_info()
        print(f"\n{'='*50}")
        print(f"DCS TRANSLATION TOOL v{info['version']}")
        print(f"{'='*50}")
        for ver, desc in info['history'].items():
            print(f"v{ver}: {desc}")
        print(f"{'='*50}\n")
